	/*
	 * For your reference:
	 *
	 * SinglyLinkedListNode {
	 *     int data;
	 *     SinglyLinkedListNode next;
	 * }
	 * 1 -> 2 -> 3 -> 4 -> null
	 */
	static boolean isCircular(SinglyLinkedListNode head) {
		
		SinglyLinkedListNode node = head.next;
		
		while(node!=null && node!=head) {
			node = node.next;
		}
		
		return node == head;
	}

